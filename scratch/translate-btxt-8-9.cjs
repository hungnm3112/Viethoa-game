function applyTranslations(chunkPath, mapData) {
  if (!fs.existsSync(chunkPath)) return;
  const chunk = JSON.parse(fs.readFileSync(chunkPath, 'utf8'));
  let appliedCount = 0;
  for (const r of chunk.replacements) {
    if (mapData[r.sourceText]) {
      r.translatedText = mapData[r.sourceText];
      appliedCount++;
    }
  }
  const fsPath = require('path');
  const outPath = chunkPath.replace('input/languages/', 'output/languages/');
  fs.mkdirSync(fsPath.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, JSON.stringify(chunk, null, 2) + '\n', 'utf8');
  console.log(`Translated ${appliedCount} strings in ${chunkPath}`);
}

applyTranslations('input/languages/chunks/btxt_chunk_8.json', mapData);
